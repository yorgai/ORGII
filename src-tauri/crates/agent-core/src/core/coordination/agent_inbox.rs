//! Typed Agent Org messaging primitives + persistent inbox.
//!
//! Substrate for inter-agent communication inside an `AgentOrgRun`. Uses
//! a typed enum (`AgentMessage`) persisted to a SQLite table
//! (`agent_inbox`). An in-process delivery channel layers on top of the
//! same store, and the sidechain-resume drain path consumes it.
//!
//! ## NOT to be confused with the `inbox` crate
//!
//! The `inbox` crate is the **user-facing** inbox: notifications and summaries
//! that an agent posts to the human via `send_to_inbox`. This module is the
//! **agent-to-agent** inbox: typed messages exchanged between sibling agents
//! inside the same `AgentOrgRun`. Different audience, different lifecycle,
//! different schema — they intentionally do not share storage or types.
//!
//! ## Design notes
//! - `AgentMessage` is a discriminated union. `Plain` is the only free-form
//!   text variant; the rest are typed RPCs that downstream code can pattern
//!   match on without re-parsing strings.
//! - Persistence stores the variant tag (`payload_kind`) alongside the
//!   serialised payload so debug / migration paths can introspect without a
//!   full JSON walk. The `payload_kind` column MUST stay in sync with the
//!   serde `tag = "kind"` on `AgentMessage` — see the `kind_tag_matches_serde_tag`
//!   regression test below.

use rusqlite::{params, Connection, Result as SqliteResult};
use serde::{Deserialize, Serialize};

use crate::session::AgentExecMode;
use database::db::get_connection;

pub fn is_supported_agent_org_remote_mode(mode: AgentExecMode) -> bool {
    matches!(
        mode,
        AgentExecMode::Build | AgentExecMode::Ask | AgentExecMode::Plan
    )
}

/// Reserved sender id for system-generated agent inbox rows.
///
/// Used by:
/// - [`AgentMessage::MemberTerminated`] — inbox-drain side-effect path
///   stamps it after observing a `ShutdownResponse{accepted=true}` and
///   cancelling the member's session.
/// - [`AgentMessage::MemberIdle`] — coordinator-side idle hook stamps
///   it when a member session transitions to idle / interrupted /
///   failed at the turn-finalize boundary.
///
/// These are out-of-band notifications — no LLM originated them — so the
/// sender field gets a marker that cannot collide with any real
/// `agent_id` (those are UUIDs / human-chosen names).
pub const SYSTEM_SENDER_ID: &str = "_system";
pub const USER_SENDER_ID: &str = "_user";

/// Wrapper around a UUID-ish string that correlates a request to a response.
///
/// Always client-generated by the sender (the LLM, in practice — it picks
/// the id and echoes it on the response). Required for every RPC-style
/// variant (`*Request`, `*Response`); ignored for `Plain` and one-shot
/// notifications.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct RequestId(pub String);

impl RequestId {
    /// Generate a fresh UUID-backed id. Used by tests today and by the
    /// response path (auto-correlation when a worker replies via a
    /// non-LLM source, e.g. an internal cancellation timeout).
    pub fn new() -> Self {
        Self(uuid::Uuid::new_v4().to_string())
    }

    pub fn as_str(&self) -> &str {
        &self.0
    }
}

impl Default for RequestId {
    fn default() -> Self {
        Self::new()
    }
}

/// Typed message payload exchanged between agents inside an `AgentOrgRun`.
///
/// Each variant has its own validation rules — see [`AgentMessage::validate`].
///
/// ## Why so few variants
/// We deliberately keep this enum minimal. orgii already has a permission
/// system (`interaction::permission`) and a mode-switch system
/// (`interaction::mode_switch`) that talk to the **user**, not other agents.
/// A second agent-bus shadow of those would be a parallel implementation
/// with no real receiver. Only variants with a concrete caller AND a
/// concrete recipient outcome survive:
///
///   * `Plain` — free-form text (the common case).
///   * `Shutdown{Request,Response}` — graceful coordinator-driven worker
///     termination.
///   * `PlanApproval{Request,Response}` — when an org member enters plan
///     mode and calls `create_plan`, the plan is routed to the
///     coordinator's inbox as a `PlanApprovalRequest` instead of lighting
///     up the user's Build button on the member's UI. The coordinator
///     then sends back a `PlanApprovalResponse` (approve / reject), and
///     the member's inbox-drain wires the response into the same
///     "exit plan mode + start a Build turn" path that the user-driven
///     `agent_plan_approval_response` Tauri command uses.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case", tag = "kind")]
pub enum AgentMessage {
    /// Free-form text. `summary` is a short label used in tool listings;
    /// `text` is the body delivered into the recipient's transcript.
    Plain { summary: String, text: String },

    /// Coordinator → worker: please wind down and exit. Response is
    /// `ShutdownResponse`.
    ShutdownRequest {
        request_id: RequestId,
        reason: Option<String>,
    },
    ShutdownResponse {
        request_id: RequestId,
        accepted: bool,
        note: Option<String>,
    },

    /// Member → coordinator: "I drafted a plan, please approve."
    /// Sent by `create_plan` when the calling session participates in
    /// an org and is not the coordinator. The coordinator's LLM is
    /// expected to reply with `PlanApprovalResponse` echoing the same
    /// `request_id`.
    ///
    /// `plan_path` is the absolute path to the plan markdown file on
    /// disk so the coordinator can read it directly with `read_file`
    /// if the inline `plan_content` excerpt is not enough. `plan_title`
    /// is the human-friendly label used by `create_plan`.
    PlanApprovalRequest {
        request_id: RequestId,
        plan_title: String,
        plan_path: String,
        plan_content: String,
    },

    /// Coordinator → member: "I reviewed your plan."
    ///
    /// `accepted = true` exits plan mode on the member side and stages
    /// the next turn mode. `accepted = false` keeps the member in plan
    /// mode so it can revise and re-submit. When omitted, `next_mode`
    /// defaults to `Build` for approvals and `Plan` for rejections.
    /// Only `Build`, `Ask`, and `Plan` are accepted for Agent
    /// Org remote mode control while Review/Debug/Wingman remain outside
    /// the coordinator scheduling contract.
    PlanApprovalResponse {
        request_id: RequestId,
        accepted: bool,
        feedback: Option<String>,
        next_mode: Option<AgentExecMode>,
    },

    /// System → coordinator: "member `<member_id>` (a.k.a. `<member_name>`)
    /// has been terminated."
    ///
    /// Emitted by the coordinator-side inbox-drain side-effect path
    /// when it observes a `ShutdownResponse{accepted=true}` from a
    /// member (after issuing `cancel_session` on the member's runtime).
    /// Without it the coordinator's LLM has no signal that the worker
    /// is gone and may keep dispatching tasks into a dead inbox.
    ///
    /// Not LLM-callable: `org_send_message` rejects this kind, and the
    /// `sender_agent_id` of the inbox row MUST equal [`SYSTEM_SENDER_ID`]
    /// (`"_system"`) so a member cannot forge another member's termination
    /// notification. Both invariants are pinned by unit + E2E tests.
    MemberTerminated {
        member_id: String,
        member_name: String,
        reason: MemberTerminationReason,
    },

    /// System → coordinator: "member `<agent_id>` is idle."
    ///
    /// Emitted at every member transition to idle so the coordinator's
    /// LLM has a positive signal that the worker finished its current
    /// turn / errored out / was interrupted, instead of having to poll
    /// session status out-of-band.
    ///
    /// Uses the same system-sender pattern as `MemberTerminated` so
    /// the worker session
    /// does not need a write handle into another session's inbox table.
    /// The coordinator-side hook in `inbox_drain` observes member-session
    /// finalize transitions and stamps the row with [`SYSTEM_SENDER_ID`].
    ///
    /// The fields cover everything orgii can faithfully populate: who,
    /// why, and the optional last-message summary the LLM uses to
    /// compose its next dispatch.
    ///
    /// Not LLM-callable: `org_send_message` rejects this kind for the
    /// same reasoning as `MemberTerminated` — allowing a member to forge
    /// peer-idle notifications would let it spoof another member's
    /// completion state and trick the coordinator into double-dispatch.
    MemberIdle {
        member_id: String,
        member_name: String,
        reason: MemberIdleReason,
        /// Execution mode the member ended the turn in. This is
        /// coordinator-facing evidence for remote mode control: after an
        /// `exec_mode_set_request`, the next `member_idle` row should show
        /// the mode that actually ran.
        current_mode: Option<AgentExecMode>,
        /// Short XML-safe blurb extracted from the member's last
        /// outbound DM this turn. `None` is allowed — turn end without
        /// an outbound DM is a valid (and common) idle transition.
        summary: Option<String>,
        /// Free-form runtime error string when `reason == Failed`.
        /// Required iff `Failed`; rejected otherwise. Validated by
        /// [`AgentMessage::validate`].
        failure_reason: Option<String>,
    },

    /// System → owner: "you have been assigned task `<task_id>`."
    ///
    /// Emitted whenever a task's `owner` field is set or changed —
    /// either by an explicit `task_update` from a peer, or implicitly
    /// when the autonomous-claim path calls `claimTask`.
    ///
    /// This row is enqueued by the `task_create`/`task_update` LLM
    /// tools and by the autonomous-claim path. It is *not* LLM-callable
    /// via `org_send_message` — fabricating an assignment would let
    /// any member redirect the coordinator's task graph. The producer
    /// stamps `sender_agent_id` with whoever caused the assignment (the
    /// assigning member's `agent_id`, or [`SYSTEM_SENDER_ID`] for
    /// autonomous self-claims).
    ///
    /// `subject` / `description` are denormalized snapshots of the
    /// assigned [`agent_org_tasks::Task`](super::agent_org_tasks::Task)
    /// row at the moment of assignment, so the recipient's LLM can
    /// render the assignment in the inbox-drain prompt without an extra
    /// store lookup. The authoritative copy lives in `agent_org_tasks`.
    /// `assigned_by` is the human-readable display name; the wire-level
    /// identity of the producer is `sender_agent_id` on the row.
    TaskAssigned {
        task_id: String,
        subject: String,
        description: String,
        assigned_by: String,
    },

    /// Coordinator → member: switch your `AgentExecMode` for the next
    /// turn (e.g. flip from `Build` to `Plan` so the member drafts a
    /// plan, or back to `Build` after a plan is approved).
    ///
    /// The surface is the inter-agent enum: the coordinator LLM picks
    /// a mode and the member's drain applies it before the next turn
    /// starts. There is no response variant: the side effect is a
    /// runtime mutation, and the next `MemberIdle` carries the mode
    /// the member actually ended the turn in (the LLM can read that to
    /// confirm the switch took).
    ///
    /// `reason` is an optional human blurb the recipient can echo into
    /// its system prompt the same turn (mirrors how
    /// `PlanApprovalResponse.feedback` is wired). Bounded length so a
    /// runaway prompt cannot blow up history.
    ExecModeSetRequest {
        request_id: RequestId,
        mode: AgentExecMode,
        reason: Option<String>,
    },
}

/// Why a member was terminated. Stable wire string so coordinator-side
/// LLM prompts can branch on it without re-parsing freeform text.
///
/// Today the only producer is the shutdown-handshake side-effect path, so
/// only the `Shutdown` variant is emitted. Other reasons (crash, cancel,
/// timeout) are reserved for follow-up phases.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum MemberTerminationReason {
    /// The member acknowledged a shutdown request (or volunteered one)
    /// via `ShutdownResponse{accepted=true}`, and the coordinator-side
    /// drain cancelled the session in response. This is the protocol
    /// happy path.
    Shutdown,
}

/// Why a member became idle.
///
/// `Available` — the member finished its turn cleanly and is ready for
/// the next dispatch. `Interrupted` — a user/coordinator cancel landed
/// mid-turn. `Failed` — the member hit a runtime error during the turn
/// and surfaces `failure_reason` for the coordinator's LLM.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum MemberIdleReason {
    Available,
    Interrupted,
    Failed,
}

impl AgentMessage {
    /// Stable discriminator string persisted alongside the JSON payload.
    /// Must round-trip with the serde tag; if the enum is renamed, update
    /// both sides simultaneously.
    pub fn kind_tag(&self) -> &'static str {
        match self {
            Self::Plain { .. } => "plain",
            Self::ShutdownRequest { .. } => "shutdown_request",
            Self::ShutdownResponse { .. } => "shutdown_response",
            Self::PlanApprovalRequest { .. } => "plan_approval_request",
            Self::PlanApprovalResponse { .. } => "plan_approval_response",
            Self::MemberTerminated { .. } => "member_terminated",
            Self::MemberIdle { .. } => "member_idle",
            Self::TaskAssigned { .. } => "task_assigned",
            Self::ExecModeSetRequest { .. } => "exec_mode_set_request",
        }
    }

    /// `RequestId` carried by RPC variants, if any.
    pub fn request_id(&self) -> Option<&RequestId> {
        match self {
            Self::ShutdownRequest { request_id, .. }
            | Self::ShutdownResponse { request_id, .. }
            | Self::PlanApprovalRequest { request_id, .. }
            | Self::PlanApprovalResponse { request_id, .. }
            | Self::ExecModeSetRequest { request_id, .. } => Some(request_id),
            Self::Plain { .. }
            | Self::MemberTerminated { .. }
            | Self::MemberIdle { .. }
            | Self::TaskAssigned { .. } => None,
        }
    }

    /// Whether this variant carries an RPC-style structured payload (vs
    /// plain free-form text). Used by `org_send_message` to enforce the
    /// "broadcast must be plain only" invariant — structured RPCs need a
    /// 1:1 correlation between sender and recipient via `request_id`,
    /// which is incoherent with fan-out.
    ///
    /// `MemberTerminated` is structured (typed payload, system-emitted),
    /// even though it has no `request_id`: the broadcast guard rejects
    /// it because broadcasting a member-termination notice to every member
    /// is nonsensical, and `org_send_message` rejects it outright as
    /// non-LLM-callable.
    pub fn is_structured(&self) -> bool {
        match self {
            Self::Plain { .. } => false,
            Self::ShutdownRequest { .. }
            | Self::ShutdownResponse { .. }
            | Self::PlanApprovalRequest { .. }
            | Self::PlanApprovalResponse { .. }
            | Self::MemberTerminated { .. }
            | Self::MemberIdle { .. }
            | Self::TaskAssigned { .. }
            | Self::ExecModeSetRequest { .. } => true,
        }
    }

    /// Validate variant invariants prior to persistence.
    ///
    /// Returns a stable error string suitable to surface back to the LLM
    /// via tool error reporting.
    pub fn validate(&self) -> Result<(), String> {
        match self {
            Self::Plain { summary, text } => {
                if summary.trim().is_empty() {
                    return Err("Plain.summary must not be empty".into());
                }
                if text.trim().is_empty() {
                    return Err("Plain.text must not be empty".into());
                }
                if summary.len() > 200 {
                    return Err("Plain.summary must be ≤ 200 chars".into());
                }
            }
            Self::ShutdownRequest { request_id, .. }
            | Self::ShutdownResponse { request_id, .. } => {
                if request_id.as_str().trim().is_empty() {
                    return Err("request_id must not be empty".into());
                }
            }
            Self::PlanApprovalRequest {
                request_id,
                plan_title,
                plan_path,
                plan_content,
            } => {
                if request_id.as_str().trim().is_empty() {
                    return Err("request_id must not be empty".into());
                }
                if plan_title.trim().is_empty() {
                    return Err("PlanApprovalRequest.plan_title must not be empty".into());
                }
                if plan_path.trim().is_empty() {
                    return Err("PlanApprovalRequest.plan_path must not be empty".into());
                }
                if plan_content.trim().is_empty() {
                    return Err("PlanApprovalRequest.plan_content must not be empty".into());
                }
            }
            Self::PlanApprovalResponse {
                request_id,
                next_mode,
                ..
            } => {
                if request_id.as_str().trim().is_empty() {
                    return Err("request_id must not be empty".into());
                }
                if let Some(mode) = next_mode {
                    if !is_supported_agent_org_remote_mode(*mode) {
                        return Err("PlanApprovalResponse.next_mode must be one of: build, ask, plan".into());
                    }
                }
            }
            Self::MemberTerminated {
                member_id,
                member_name,
                ..
            } => {
                if member_id.trim().is_empty() {
                    return Err("MemberTerminated.member_id must not be empty".into());
                }
                if member_name.trim().is_empty() {
                    return Err("MemberTerminated.member_name must not be empty".into());
                }
            }
            Self::MemberIdle {
                member_id,
                member_name,
                reason,
                summary,
                failure_reason,
                ..
            } => {
                if member_id.trim().is_empty() {
                    return Err("MemberIdle.member_id must not be empty".into());
                }
                if member_name.trim().is_empty() {
                    return Err("MemberIdle.member_name must not be empty".into());
                }
                if let Some(s) = summary {
                    if s.len() > 500 {
                        return Err("MemberIdle.summary must be ≤ 500 chars".into());
                    }
                }
                match reason {
                    MemberIdleReason::Failed => {
                        let fr = failure_reason.as_deref().unwrap_or("").trim();
                        if fr.is_empty() {
                            return Err(
                                "MemberIdle.failure_reason is required when reason='failed'".into(),
                            );
                        }
                    }
                    MemberIdleReason::Available | MemberIdleReason::Interrupted => {
                        if failure_reason.is_some() {
                            return Err(
                                "MemberIdle.failure_reason is only valid when reason='failed'"
                                    .into(),
                            );
                        }
                    }
                }
            }
            Self::TaskAssigned {
                task_id,
                subject,
                assigned_by,
                description,
            } => {
                if task_id.trim().is_empty() {
                    return Err("TaskAssigned.task_id must not be empty".into());
                }
                if subject.trim().is_empty() {
                    return Err("TaskAssigned.subject must not be empty".into());
                }
                if assigned_by.trim().is_empty() {
                    return Err("TaskAssigned.assigned_by must not be empty".into());
                }
                if subject.len() > 200 {
                    return Err("TaskAssigned.subject must be ≤ 200 chars".into());
                }
                if description.len() > 4000 {
                    return Err("TaskAssigned.description must be ≤ 4000 chars".into());
                }
            }
            Self::ExecModeSetRequest {
                request_id,
                mode,
                reason,
            } => {
                if request_id.as_str().trim().is_empty() {
                    return Err("ExecModeSetRequest.request_id must not be empty".into());
                }
                if !is_supported_agent_org_remote_mode(*mode) {
                    return Err(
                        "ExecModeSetRequest.mode must be one of: build, ask, plan".into(),
                    );
                }
                if let Some(r) = reason {
                    if r.len() > 500 {
                        return Err("ExecModeSetRequest.reason must be ≤ 500 chars".into());
                    }
                }
            }
        }
        Ok(())
    }
}

/// Persisted inbox row.
#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct AgentInboxRecord {
    pub id: i64,
    pub recipient_agent_id: String,
    pub recipient_member_id: Option<String>,
    pub sender_agent_id: String,
    pub sender_member_id: Option<String>,
    pub org_run_id: Option<String>,
    pub payload_kind: String,
    pub payload_json: String,
    pub request_id: Option<String>,
    pub created_at: String,
    pub read_at: Option<String>,
}

impl AgentInboxRecord {
    /// Re-hydrate the typed `AgentMessage` from the persisted JSON
    /// payload. Returns a stable error string on schema drift; callers
    /// surface this through whichever transport applies (debug endpoint,
    /// LLM tool-error, drain-loop log).
    pub fn decode_payload(&self) -> Result<AgentMessage, String> {
        serde_json::from_str(&self.payload_json)
            .map_err(|err| format!("decode AgentMessage payload (id={}) failed: {err}", self.id))
    }
}

/// Insertion parameters for the inbox.
#[derive(Debug, Clone)]
pub struct InsertInboxParams {
    pub recipient_agent_id: String,
    pub recipient_member_id: Option<String>,
    pub sender_agent_id: String,
    pub sender_member_id: Option<String>,
    pub org_run_id: Option<String>,
    pub message: AgentMessage,
}

/// Initialize the `agent_inbox` table.
///
/// Hot-path indexes:
/// - `(recipient_member_id, read_at, created_at)` — materialized org member drain query.
/// - `(recipient_agent_id, read_at, created_at)` — coordinator / legacy drain query.
/// - `(org_run_id, created_at)` — debug / E2E "list every message in this
///   run" view.
/// - `(request_id)` — RPC correlation lookups.
pub fn init_schema(conn: &Connection) -> SqliteResult<()> {
    create_agent_inbox_table(conn)?;
    conn.execute_batch(
        "CREATE INDEX IF NOT EXISTS idx_agent_inbox_recipient_member_unread
            ON agent_inbox(recipient_member_id, read_at, created_at);
        CREATE INDEX IF NOT EXISTS idx_agent_inbox_recipient_unread
            ON agent_inbox(recipient_agent_id, read_at, created_at);
        CREATE INDEX IF NOT EXISTS idx_agent_inbox_org_run
            ON agent_inbox(org_run_id, created_at);
        CREATE INDEX IF NOT EXISTS idx_agent_inbox_request_id
            ON agent_inbox(request_id);",
    )
}

fn create_agent_inbox_table(conn: &Connection) -> SqliteResult<()> {
    conn.execute_batch(
        "CREATE TABLE IF NOT EXISTS agent_inbox (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            recipient_agent_id TEXT NOT NULL,
            recipient_member_id TEXT,
            sender_agent_id TEXT NOT NULL,
            sender_member_id TEXT,
            org_run_id TEXT,
            payload_kind TEXT NOT NULL,
            payload_json TEXT NOT NULL,
            request_id TEXT,
            created_at TEXT NOT NULL,
            read_at TEXT
        );",
    )
}

pub struct AgentInboxStore;

impl AgentInboxStore {
    /// Persist a message and return the inserted record. The caller is
    /// responsible for resolving display-name / broadcast targets to one or
    /// more concrete `AgentId`s before calling this — the store does not
    /// fan out by itself. The router owns that resolution.
    pub fn insert(params: InsertInboxParams) -> Result<AgentInboxRecord, String> {
        params.message.validate()?;

        let kind = params.message.kind_tag().to_string();
        let request_id = params.message.request_id().map(|r| r.0.clone());
        let payload_json = serde_json::to_string(&params.message)
            .map_err(|err| format!("serialize AgentMessage failed: {err}"))?;
        let now = chrono::Utc::now().to_rfc3339();

        let conn = get_connection().map_err(|err| err.to_string())?;
        conn.execute(
            "INSERT INTO agent_inbox (
                recipient_agent_id,
                recipient_member_id,
                sender_agent_id,
                sender_member_id,
                org_run_id,
                payload_kind,
                payload_json,
                request_id,
                created_at,
                read_at
            ) VALUES (?1, ?2, ?3, ?4, ?5, ?6, ?7, ?8, ?9, NULL)",
            params![
                &params.recipient_agent_id,
                params.recipient_member_id.as_deref(),
                &params.sender_agent_id,
                params.sender_member_id.as_deref(),
                params.org_run_id.as_deref(),
                &kind,
                &payload_json,
                request_id.as_deref(),
                &now,
            ],
        )
        .map_err(|err| err.to_string())?;

        let id = conn.last_insert_rowid();
        Ok(AgentInboxRecord {
            id,
            recipient_agent_id: params.recipient_agent_id,
            recipient_member_id: params.recipient_member_id,
            sender_agent_id: params.sender_agent_id,
            sender_member_id: params.sender_member_id,
            org_run_id: params.org_run_id,
            payload_kind: kind,
            payload_json,
            request_id,
            created_at: now,
            read_at: None,
        })
    }
}

impl AgentInboxStore {
    /// Return every inbox row for a given Agent Org run, ordered by
    /// insert time. Production callers:
    ///
    /// - `/test/agent-org/inbox/list-by-run` debug endpoint — drives
    ///   the inter-agent E2E observability path.
    pub fn list_by_run(org_run_id: &str) -> Result<Vec<AgentInboxRecord>, String> {
        let conn = get_connection().map_err(|err| err.to_string())?;
        let mut stmt = conn
            .prepare(
                "SELECT id,
                        recipient_agent_id,
                        recipient_member_id,
                        sender_agent_id,
                        sender_member_id,
                        org_run_id,
                        payload_kind,
                        payload_json,
                        request_id,
                        created_at,
                        read_at
                 FROM agent_inbox
                 WHERE org_run_id = ?1
                 ORDER BY id ASC",
            )
            .map_err(|err| err.to_string())?;
        let rows = stmt
            .query_map(params![org_run_id], row_to_record)
            .map_err(|err| err.to_string())?;
        let mut out = Vec::new();
        for row in rows {
            out.push(row.map_err(|err| err.to_string())?);
        }
        Ok(out)
    }
}

fn row_to_record(row: &rusqlite::Row<'_>) -> SqliteResult<AgentInboxRecord> {
    Ok(AgentInboxRecord {
        id: row.get(0)?,
        recipient_agent_id: row.get(1)?,
        recipient_member_id: row.get(2)?,
        sender_agent_id: row.get(3)?,
        sender_member_id: row.get(4)?,
        org_run_id: row.get(5)?,
        payload_kind: row.get(6)?,
        payload_json: row.get(7)?,
        request_id: row.get(8)?,
        created_at: row.get(9)?,
        read_at: row.get(10)?,
    })
}

impl AgentInboxStore {
    pub fn list_unread_for_member(
        recipient_member_id: &str,
        org_run_id: &str,
    ) -> Result<Vec<AgentInboxRecord>, String> {
        let conn = get_connection().map_err(|err| err.to_string())?;
        let mut stmt = conn
            .prepare(
                "SELECT id,
                        recipient_agent_id,
                        recipient_member_id,
                        sender_agent_id,
                        sender_member_id,
                        org_run_id,
                        payload_kind,
                        payload_json,
                        request_id,
                        created_at,
                        read_at
                 FROM agent_inbox
                 WHERE recipient_member_id = ?1
                   AND org_run_id = ?2
                   AND read_at IS NULL
                 ORDER BY id ASC",
            )
            .map_err(|err| err.to_string())?;
        let rows = stmt
            .query_map(params![recipient_member_id, org_run_id], row_to_record)
            .map_err(|err| err.to_string())?;
        let mut out = Vec::new();
        for row in rows {
            out.push(row.map_err(|err| err.to_string())?);
        }
        Ok(out)
    }

    /// Mark a batch of inbox rows as read in a single immediate
    /// transaction. Idempotent: rows that are already read return
    /// `0` updates and do not error. Returns the total number of rows
    /// whose `read_at` was actually advanced.
    ///
    /// Used by the turn-processor drain hook after rendering the
    /// attachment, so the next turn's drain returns an empty list.
    pub fn mark_many_read(ids: &[i64]) -> Result<usize, String> {
        if ids.is_empty() {
            return Ok(0);
        }
        let mut conn = get_connection().map_err(|err| err.to_string())?;
        let tx = conn
            .transaction_with_behavior(rusqlite::TransactionBehavior::Immediate)
            .map_err(|err| err.to_string())?;
        let now = chrono::Utc::now().to_rfc3339();
        let mut updated = 0usize;
        {
            let mut stmt = tx
                .prepare("UPDATE agent_inbox SET read_at = ?1 WHERE id = ?2 AND read_at IS NULL")
                .map_err(|err| err.to_string())?;
            for id in ids {
                let n = stmt
                    .execute(params![&now, id])
                    .map_err(|err| err.to_string())?;
                updated += n;
            }
        }
        tx.commit().map_err(|err| err.to_string())?;
        Ok(updated)
    }
}

