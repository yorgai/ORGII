/// Sync project conventions (`.orgii/agent-rules.md`) to each CLI agent's
/// native rules file.
///
/// Replaces the old `sync_conventions_to_cursorrules()` which wrote to the
/// deprecated `.cursorrules` file (which Cursor agent mode ignores).
pub fn sync_conventions_for_agent(agent: &ModelType, workspace_path: &Path) -> Vec<PathBuf> {
    let conventions_path = workspace_path.join(".orgii").join("agent-rules.md");
    let content = match fs::read_to_string(&conventions_path) {
        Ok(content) if !content.trim().is_empty() => content,
        _ => return Vec::new(),
    };

    let targets = convention_targets_for_agent(agent, workspace_path);
    let mut written: Vec<PathBuf> = Vec::new();

    for target in targets {
        // Skip if a non-orgii file already exists at this path
        if target.exists() {
            let is_ours = fs::read_to_string(&target)
                .map(|existing| existing.contains(ORGII_MARKER))
                .unwrap_or(false);
            if !is_ours {
                continue;
            }
        }

        match write_rule_file(&target, &content) {
            Ok(()) => {
                tracing::info!("[skill_sync] Synced conventions to {}", target.display());
                written.push(target);
            }
            Err(err) => {
                tracing::warn!(
                    "[skill_sync] Failed to sync conventions to {}: {}",
                    target.display(),
                    err
                );
            }
        }
    }

    written
}

/// Determine convention file paths for each agent type.
fn convention_targets_for_agent(agent: &ModelType, workspace_path: &Path) -> Vec<PathBuf> {
    match agent {
        ModelType::CursorCli => {
            vec![workspace_path
                .join(".cursor")
                .join("rules")
                .join("orgii-conventions.mdc")]
        }
        ModelType::ClaudeCode => {
            vec![workspace_path
                .join(".claude")
                .join("rules")
                .join("orgii-conventions.md")]
        }
        ModelType::Codex => {
            vec![workspace_path.join(".codex").join("orgii-conventions.md")]
        }
        ModelType::GeminiCli => {
            vec![workspace_path.join(".gemini").join("orgii-conventions.md")]
        }
        ModelType::Copilot => {
            vec![workspace_path
                .join(".github")
                .join("instructions")
                .join("orgii-conventions.instructions.md")]
        }
        ModelType::Kiro => Vec::new(),
        _ => Vec::new(),
    }
}
