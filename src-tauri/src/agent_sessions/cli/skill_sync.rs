/// Build the formatted skills content shared by all agents.
fn build_skills_content(
    loader: &SkillsLoader,
    skills: &[SkillInfo],
    disabled_skills: &[String],
) -> String {
    let mut parts: Vec<String> = Vec::new();

    let always_sections = loader.build_always_skills_manifest_section(disabled_skills, None);
    parts.extend(always_sections);

    // Summary of all available skills with scan instructions
    let summary_lines: Vec<String> = skills
        .iter()
        .map(|skill| {
            let status = if skill.available {
                "available"
            } else {
                "unavailable"
            };
            let desc = if skill.description.is_empty() {
                "No description".to_string()
            } else {
                skill.description.clone()
            };
            format!(
                "- **{}** ({}): {} [{}]\n  Path: `{}`",
                skill.name,
                skill.source,
                desc,
                status,
                skill.path.display()
            )
        })
        .collect();

    parts.push(format!(
        "## Skills (mandatory)\n\n\
         Before replying: scan the skill descriptions below.\n\
         - If exactly one skill clearly applies: read its SKILL.md using read_file, then follow it.\n\
         - If multiple could apply: choose the most specific one, then read/follow it.\n\
         - If none clearly apply: do not read any SKILL.md.\n\
         Constraints: never read more than one skill up front; only read after selecting.\n\n\
         {}",
        summary_lines.join("\n")
    ));

    parts.join("\n\n---\n\n")
}

/// Write a rule file with the appropriate wrapper for its format.
fn write_rule_file(target: &Path, content: &str) -> Result<(), String> {
    // Ensure parent directory exists
    if let Some(parent) = target.parent() {
        if !parent.exists() {
            fs::create_dir_all(parent).map_err(|err| {
                format!("Failed to create directory {}: {}", parent.display(), err)
            })?;
        }
    }

    let ext = target.extension().and_then(|e| e.to_str()).unwrap_or("");
    let wrapped = match ext {
        "mdc" => format_mdc(content),
        _ => format_markdown(content),
    };

    fs::write(target, wrapped)
        .map_err(|err| format!("Failed to write {}: {}", target.display(), err))
}

/// Wrap content as a Cursor `.mdc` file with `alwaysApply: true` frontmatter.
fn format_mdc(content: &str) -> String {
    format!(
        "---\ndescription: Skills injected by orgii for coding sessions\nalwaysApply: true\n---\n\n{marker}\n\n{content}",
        marker = ORGII_MARKER,
        content = content,
    )
}

/// Wrap content as a plain markdown file with the orgii marker.
fn format_markdown(content: &str) -> String {
    format!(
        "{marker}\n\n{content}",
        marker = ORGII_MARKER,
        content = content,
    )
}

